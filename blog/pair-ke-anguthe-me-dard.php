<h2>पैर के अंगूठे में दर्द के कारण</h2>
<p>पैर के अंगूठे में दर्द कोई अकेली बीमारी नहीं, बल्कि कई अलग-अलग कारणों का लक्षण है। सही इलाज के लिए सही कारण पहचानना सबसे जरूरी कदम है:</p>

<h3>1. यूरिक एसिड बढ़ना / गाउट (सबसे आम कारण)</h3>
<p>पैर के अंगूठे में अचानक तेज दर्द का सबसे आम कारण गाउट है। जब शरीर में यूरिक एसिड बढ़ जाता है, तो यह छोटे-छोटे क्रिस्टल बनकर पैर के अंगूठे के जोड़ में जमा हो जाता है। इससे आमतौर पर <strong>रात के समय अचानक अंगूठे के जोड़ में बहुत तेज दर्द, लालिमा, सूजन और गर्माहट</strong> हो जाती है। अक्सर मरीज बताते हैं कि दर्द इतना तेज होता है कि चादर का हल्का स्पर्श भी बर्दाश्त नहीं होता। यह गाउट का सबसे विशिष्ट लक्षण है और इसे नजरअंदाज नहीं करना चाहिए।</p>

<h3>2. गठिया / अर्थराइटिस (Arthritis)</h3>
<p>गठिया या अर्थराइटिस में अंगूठे समेत पैर के छोटे जोड़ों में सूजन और दर्द होता है। ऑस्टियोअर्थराइटिस में जोड़ की गद्दी (कार्टिलेज) घिसने से अंगूठे में अकड़न और दर्द रहता है, जबकि रुमेटाइड अर्थराइटिस में शरीर की रोग-प्रतिरोधक प्रणाली ही जोड़ों पर हमला करती है। ऐसे में सुबह के समय जोड़ों में जकड़न ज्यादा महसूस होती है। जोड़ों के दर्द के इलाज के बारे में विस्तार से जानने के लिए हमारा लेख <a href="/blog/jodon-ke-dard-ka-sabse-accha-ilaj-kya-hai">जोड़ों के दर्द का सबसे अच्छा इलाज क्या है</a> जरूर पढ़ें।</p>

<h3>3. अंगूठे में चोट या फ्रैक्चर (Injury / Fracture)</h3>
<p>किसी सख्त चीज से अंगूठा टकराना, भारी वस्तु का गिरना या ठोकर लगना अंगूठे में चोट, मोच या हड्डी टूटने (फ्रैक्चर) का कारण बन सकता है। ऐसी स्थिति में चोट वाली जगह पर सूजन, नीला निशान और चलने-फिरने में तेज दर्द होता है। अगर चोट के बाद अंगूठे पर वजन डालना मुश्किल हो, तो एक्स-रे जरूर करवाना चाहिए।</p>

<h3>4. इनग्रोन टोनेल / नाखून का मांस में धंसना (Ingrown Toenail)</h3>
<p>जब पैर के अंगूठे का नाखून गलत तरीके से बढ़कर आसपास के मांस में धंस जाता है, तो इसे इनग्रोन टोनेल कहते हैं। यह गलत तरीके से नाखून काटने या टाइट जूते पहनने से होता है। इसमें नाखून के किनारे पर दर्द, लालिमा, सूजन और कभी-कभी पस (मवाद) भी बन जाता है।</p>

<h3>5. बर्साइटिस / बनियन (Bunion)</h3>
<p>बनियन में पैर के अंगूठे के जोड़ के पास हड्डी का उभार बन जाता है, जिससे अंगूठा दूसरी उंगलियों की ओर झुकने लगता है। यह अक्सर टाइट, नुकीले या ऊँची एड़ी वाले जूते पहनने से होता है। जोड़ के पास मौजूद तरल की थैली (बर्सा) में सूजन को बर्साइटिस कहते हैं, जिससे उभार वाली जगह पर दर्द और जलन होती है।</p>

<h3>6. डायबिटिक न्यूरोपैथी (Diabetic Neuropathy)</h3>
<p>लंबे समय तक डायबिटीज रहने पर हाई ब्लड शुगर के कारण पैर की नसें कमजोर हो जाती हैं, जिसे डायबिटिक न्यूरोपैथी कहते हैं। इसमें अंगूठे और उंगलियों में दर्द के साथ-साथ जलन, झुनझुनी और सुन्नपन महसूस होता है। डायबिटीज के मरीजों को अंगूठे के किसी भी घाव या दर्द को गंभीरता से लेना चाहिए, क्योंकि इनमें घाव जल्दी नहीं भरते।</p>

<h3>7. इन्फेक्शन (Infection)</h3>
<p>अंगूठे में किसी कट, फफोले या नाखून की समस्या के कारण बैक्टीरियल इन्फेक्शन हो सकता है, जिससे अंगूठा लाल, गर्म और बहुत दर्दनाक हो जाता है। कभी-कभी जोड़ के अंदर तक इन्फेक्शन पहुँचने (सेप्टिक अर्थराइटिस) पर बुखार भी आ सकता है, जो एक आपातकालीन स्थिति है।</p>

<h2>लक्षण</h2>
<p>पैर के अंगूठे में दर्द के साथ दिखने वाले आम लक्षण इस प्रकार हैं:</p>
<ul>
<li>रात में अचानक अंगूठे के जोड़ में तेज, चुभने वाला दर्द (गाउट का मुख्य लक्षण)।</li>
<li>अंगूठे के जोड़ पर लालिमा, सूजन और छूने पर गर्माहट।</li>
<li>अंगूठे को हिलाने या उस पर वजन डालने पर दर्द बढ़ना।</li>
<li>जोड़ के आसपास की त्वचा का चमकदार और तना हुआ दिखना।</li>
<li>चलने या जूते पहनने में परेशानी।</li>
<li>बार-बार दर्द का दौरा पड़ना और फिर कुछ दिन आराम रहना।</li>
<li>कभी-कभी हल्का बुखार या पूरे शरीर में थकान का अहसास।</li>
</ul>

<h2>जांच (Diagnosis)</h2>
<p>पैर के अंगूठे में दर्द के सही कारण का पता लगाने के लिए डॉक्टर शारीरिक जांच के साथ कुछ टेस्ट करवा सकते हैं:</p>
<ul>
<li><strong>यूरिक एसिड ब्लड टेस्ट:</strong> गाउट की पुष्टि के लिए खून में यूरिक एसिड का स्तर जांचा जाता है।</li>
<li><strong>शारीरिक जांच:</strong> डॉक्टर जोड़ की सूजन, लालिमा, गर्माहट और हिलने-डुलने की क्षमता देखते हैं।</li>
<li><strong>एक्स-रे (X-Ray):</strong> चोट, फ्रैक्चर, बनियन या जोड़ के घिसाव का पता लगाने के लिए।</li>
<li><strong>ब्लड शुगर (HbA1c):</strong> डायबिटीज और न्यूरोपैथी की जांच के लिए।</li>
<li><strong>जॉइंट फ्लूइड टेस्ट:</strong> जरूरत पड़ने पर जोड़ से तरल निकालकर यूरिक एसिड क्रिस्टल या इन्फेक्शन की जांच।</li>
<li><strong>अन्य ब्लड टेस्ट:</strong> सूजन (CRP/ESR) और रुमेटाइड फैक्टर की जांच, जब अर्थराइटिस का शक हो।</li>
</ul>

<h2>घरेलू उपाय और परहेज</h2>
<p>शुरुआती दर्द और गाउट के दौरे में ये उपाय और परहेज बहुत असरदार हैं। ध्यान रहे — ये उपाय डॉक्टरी इलाज के सहायक हैं, विकल्प नहीं:</p>

<h3>1. भरपूर पानी और आराम</h3>
<p>दिनभर में खूब पानी (लगभग 8-10 गिलास) पिएं, क्योंकि पानी शरीर से अतिरिक्त यूरिक एसिड को बाहर निकालने में मदद करता है। दर्द के दौरान प्रभावित पैर को आराम दें और उसे तकिये पर थोड़ा ऊँचा रखें, इससे सूजन कम होती है।</p>

<h3>2. बर्फ से सिकाई (Ice Compression)</h3>
<p>एक पतले कपड़े में बर्फ लपेटकर सूजे हुए अंगूठे पर 15-20 मिनट सिकाई करें। यह गाउट के दर्द और सूजन को तुरंत कम करने में मदद करता है। (डायबिटीज के मरीज त्वचा की सुन्नता के कारण सावधानी बरतें)।</p>

<h3>3. डाइट में परहेज — यूरिक एसिड कम करने के लिए क्या खाएं और क्या न खाएं</h3>
<p>गाउट को नियंत्रित करने में सही खान-पान सबसे बड़ी भूमिका निभाता है। यूरिक एसिड बढ़ाने वाले फूड्स से परहेज करना और फायदेमंद चीजें खाना जरूरी है:</p>
<p><strong>इनसे परहेज करें (यूरिक एसिड बढ़ाते हैं):</strong> रेड मीट (मटन), ऑर्गन मीट (कलेजी), सीफूड और झींगा, शराब — खासकर बीयर, ज्यादा मीठे पेय और सोडा (फ्रुक्टोज युक्त), तथा बहुत ज्यादा तली-भुनी चीजें।</p>
<p><strong>ये फायदेमंद हैं (यूरिक एसिड नियंत्रित रखते हैं):</strong> खूब पानी और तरल पदार्थ, कम फैट वाली डेयरी (टोंड दूध, दही), चेरी और खट्टे फल (विटामिन C युक्त), ताजी सब्जियां, साबुत अनाज और पर्याप्त फाइबर।</p>

<h3>4. सही जूते और नाखून की देखभाल</h3>
<p>आरामदायक, खुले और सही नाप के जूते-चप्पल पहनें ताकि अंगूठे पर दबाव न पड़े। इनग्रोन टोनेल से बचने के लिए नाखून को सीधा (गोल नहीं) और बहुत छोटा न काटें। बनियन की समस्या में नुकीले और ऊँची एड़ी वाले जूतों से बचें।</p>

<h3>5. वजन नियंत्रण और सक्रिय जीवनशैली</h3>
<p>ज्यादा वजन जोड़ों पर दबाव और यूरिक एसिड दोनों बढ़ाता है। संतुलित आहार और हल्के व्यायाम से वजन नियंत्रित रखें। अगर आपके पैरों में सूजन की भी शिकायत रहती है, तो हमारा लेख <a href="/blog/pair-me-sujan">पैरों में सूजन के कारण और उपाय</a> पढ़ें, और तलवे से जुड़े दर्द के लिए <a href="/blog/pair-ke-talwe-me-dard">पैर के तलवे में दर्द के कारण और उपाय</a> भी देखें।</p>

