<h4 class="text-danger font-weight-bold"><i class="fas fa-exclamation-triangle me-2"></i> डॉक्टर के पास कब जाएं?</h4>
<p class="mb-2 text-dark">चक्कर के साथ नीचे दिए गए कोई भी संकेत दिखें, तो इसे साधारण चक्कर न समझें — यह ब्रेन स्ट्रोक या हृदय की गंभीर समस्या हो सकती है। बिना देर किए नज़दीकी अस्पताल की इमरजेंसी में जाएँ:</p>
<ul class="mb-0 text-dark">
<li>सीने में दर्द, भारीपन या साँस फूलना।</li>
<li>बोलने में लड़खड़ाहट, चेहरा टेढ़ा होना या शरीर के एक तरफ कमजोरी/सुन्नपन।</li>
<li>अचानक बहुत तेज़ सिरदर्द, धुंधला या दोहरा दिखना।</li>
<li>बेहोशी, बार-बार गिरना, या चक्कर के साथ उल्टी न रुकना।</li>
<li>दिल की धड़कन बहुत तेज़, धीमी या अनियमित महसूस होना।</li>
</ul>
