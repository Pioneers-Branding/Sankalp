<h2>पेशाब में जलन के कारण</h2>
<p>पेशाब में जलन के कारण एक नहीं बल्कि कई हो सकते हैं। सही इलाज के लिए असली वजह जानना जरूरी है। सबसे आम कारण निम्नलिखित हैं:</p>

<h3>1. यूरिन इन्फेक्शन (Urinary Tract Infection - UTI)</h3>
<p>पेशाब में जलन का सबसे आम कारण मूत्र मार्ग का संक्रमण है। इसमें बैक्टीरिया (आमतौर पर E. coli) मूत्राशय (Bladder) या मूत्रनली (Urethra) में पहुंचकर सूजन पैदा करते हैं। इससे पेशाब करते समय जलन, बार-बार पेशाब आना और पेट के निचले हिस्से में भारीपन महसूस होता है।</p>

<h3>2. पानी की कमी (Dehydration)</h3>
<p>दिनभर में पर्याप्त पानी न पीने से पेशाब गाढ़ा और गहरे पीले रंग का हो जाता है। ऐसा गाढ़ा पेशाब मूत्रनली में जलन पैदा करता है। गर्मियों में और ज्यादा पसीना आने पर यह समस्या और बढ़ जाती है।</p>

<h3>3. मूत्र मार्ग या किडनी की पथरी (Kidney/Urinary Stone)</h3>
<p>पेशाब के रास्ते में मौजूद छोटी पथरी मूत्रनली की भीतरी परत को खरोंच सकती है, जिससे जलन के साथ तेज दर्द और कभी-कभी पेशाब में खून भी आ सकता है। ऐसी जलन के साथ अक्सर कमर या बाजू (Flank) में रुक-रुककर तेज दर्द उठता है।</p>

<h3>4. तीखा-मसालेदार भोजन और कम पानी</h3>
<p>बहुत ज्यादा तीखा, मसालेदार, कैफीन (चाय-कॉफी) या शराब का सेवन पेशाब को अम्लीय (Acidic) बना देता है और मूत्राशय में जलन पैदा कर सकता है, खासकर जब साथ में पानी कम पिया जा रहा हो।</p>

<h3>5. डायबिटीज (हाई ब्लड शुगर)</h3>
<p>अनियंत्रित मधुमेह में पेशाब के जरिए शरीर से शुगर निकलती है, जो बैक्टीरिया के पनपने के लिए अनुकूल माहौल बनाती है। इसी कारण डायबिटीज के मरीजों में बार-बार यूरिन इन्फेक्शन और पेशाब में जलन होना आम है।</p>

<h3>6. यौन संचारित संक्रमण (STI) और अन्य कारण</h3>
<p>असुरक्षित यौन संबंध से होने वाले संक्रमण (जैसे गोनोरिया, क्लैमीडिया) भी पेशाब में जलन और गुप्तांग से स्राव का कारण बन सकते हैं। इसके अलावा साबुन/स्प्रे से एलर्जी, महिलाओं में मेनोपॉज के बाद हार्मोन बदलाव और पुरुषों में प्रोस्टेट का बढ़ना (Prostate Enlargement) भी जलन पैदा करते हैं।</p>

<h2>यूरिन इन्फेक्शन (UTI) के लक्षण</h2>
<p>चूंकि UTI ही पेशाब में जलन का सबसे बड़ा कारण है, इसके लक्षणों को पहचानना जरूरी है। यूरिन इन्फेक्शन (UTI) के लक्षण इस प्रकार हैं:</p>
<ul>
<li>पेशाब करते समय जलन, चुभन या दर्द होना।</li>
<li>बार-बार पेशाब जाने की इच्छा, लेकिन हर बार बहुत कम पेशाब आना।</li>
<li>पेशाब का रंग गंदला (Cloudy) होना या उसमें तेज बदबू आना।</li>
<li>पेट के निचले हिस्से या पेड़ू में भारीपन और दबाव महसूस होना।</li>
<li>कभी-कभी पेशाब में हल्का खून आना।</li>
<li>यदि संक्रमण किडनी तक पहुंच जाए तो बुखार, कंपकंपी, उल्टी और कमर में दर्द होना।</li>
</ul>

<h2>पेशाब में जलन और दर्द — किस कारण की ओर इशारा?</h2>
<p>पेशाब में जलन और दर्द के साथ जुड़े अन्य लक्षण असली कारण की ओर इशारा करते हैं। नीचे दी गई तालिका से आप अपने लक्षणों के आधार पर संभावित कारण को शुरुआती तौर पर पहचान सकते हैं। ध्यान दें कि यह केवल समझ के लिए है — सटीक निदान के लिए पेशाब की जांच (Urine Routine/Culture) जरूरी है।</p>

