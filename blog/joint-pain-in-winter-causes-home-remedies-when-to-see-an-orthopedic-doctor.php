    <h2>Frequently Asked Questions</h2>

    <div class="faq-item">
      <h4>Q: Why do my joints hurt more in the morning during winter?</h4>
      <p>A: Morning joint stiffness is normal, but it often worsens in winter because joints cool down overnight while you're sleeping. Cold ambient temperature thickens synovial fluid, and muscles become tighter without movement. Spending the night under blankets may help, as does a warm shower or bath first thing in the morning to heat and loosen joints before activity.</p>
    </div>

    <div class="faq-item">
      <h4>Q: Can cold weather cause arthritis?</h4>
      <p>A: Cold weather itself doesn't cause arthritis, but it can exacerbate existing symptoms in people who already have joint conditions. Osteoarthritis and other forms of arthritis develop over years from wear and tear, genetics, injuries, or autoimmune responses. However, once you have arthritis, cold weather can definitely make the pain feel worse.</p>
    </div>

    <div class="faq-item">
      <h4>Q: Does exercise help or hurt joint pain?</h4>
      <p>A: Exercise generally helps joint pain when done correctly. Movement lubricates joints, strengthens supporting muscles, and improves circulation. However, high-impact activities can worsen joint pain. Low-impact exercises like swimming, cycling, and walking are ideal. Always warm up before exercise and stop if you experience sharp or worsening pain.</p>
    </div>

    <div class="faq-item">
      <h4>Q: Are hot or cold treatments better for winter joint pain?</h4>
      <p>A: Heat is typically more beneficial for chronic winter joint pain because it improves circulation, relaxes muscles, and counters the constricting effects of cold weather. Apply heat for 15-20 minutes before activity or before bed. Cold therapy is generally better for acute injuries or flare-ups of inflammatory conditions like gout.</p>
    </div>

    <div class="faq-item">
      <h4>Q: How much vitamin D do I need for joint health?</h4>
      <p>A: Vitamin D is essential for calcium absorption and bone health. The recommended daily intake for adults is 600-800 IU, though many healthcare providers recommend 1000-2000 IU daily, especially in winter when sun exposure is limited. Foods like fatty fish, egg yolks, and fortified dairy products can help, but many people need supplements to maintain adequate levels.</p>
    </div>

    <div class="faq-item">
      <h4>Q: Can losing weight really reduce joint pain?</h4>
      <p>A: Absolutely. Every pound of body weight adds pressure to weight-bearing joints. Losing just 10 pounds can reduce knee stress by 30-40 pounds during walking. Weight loss also reduces inflammation since fat tissue produces inflammatory chemicals. Even modest weight loss combined with exercise can significantly improve joint function and reduce pain.</p>
    </div>

    <div class="faq-item">
      <h4>Q: Is it safe to use a heating pad on my joints every day?</h4>
      <p>A: Yes, regular heat therapy is generally safe for chronic joint pain. Use moderate heat (not too hot to cause burns), apply for 15-20 minutes at a time, and allow skin to cool between applications. Never sleep with an electric heating pad, as prolonged heat exposure can cause burns. For acute injuries or swollen joints, use cold therapy instead.</p>
