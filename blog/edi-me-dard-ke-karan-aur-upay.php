<p>यह तालिका केवल शुरुआती समझ के लिए है। सही कारण की पुष्टि जांच (X-ray, ब्लड टेस्ट) और डॉक्टर की जांच के बाद ही होती है। आइए मुख्य कारणों को विस्तार से जानें।</p>

<h3>1. प्लांटर फैसिआइटिस — सबसे आम कारण</h3>
<p>एड़ी दर्द के 10 में से लगभग 8 मामले प्लांटर फैसिआइटिस के होते हैं। यह दर्द एड़ी के ठीक नीचे, तलवे की तरफ महसूस होता है और देर तक खड़े रहने वालों, दौड़ने वालों तथा फ्लैट फुट (Flat Feet) वाले लोगों में आम है।</p>

<h3>2. हील स्पर (एड़ी की हड्डी का बढ़ना)</h3>
<p>लंबे समय तक चलने वाली सूजन के कारण एड़ी की हड्डी पर कैल्शियम जमा होकर एक नुकीला उभार (Bone Spur) बन जाता है। यह अक्सर प्लांटर फैसिआइटिस के साथ ही होता है और चलने पर चुभन वाला दर्द देता है।</p>

<h3>3. एच्लीस टेंडिनाइटिस</h3>
<p>एड़ी के पीछे को पिंडली की मांसपेशी से जोड़ने वाली मोटी टेंडन (Achilles Tendon) में खिंचाव या सूजन आने से एड़ी के पिछले हिस्से में दर्द और अकड़न होती है। यह अचानक ज्यादा दौड़ने-कूदने वालों में अधिक होता है।</p>

<h3>4. गाउट (यूरिक एसिड) और अन्य कारण</h3>
<p>शरीर में यूरिक एसिड बढ़ने (Gout) से एड़ी और जोड़ों में अचानक तेज दर्द, लाली और सूजन हो सकती है। इसके अलावा मोटापा (अतिरिक्त वजन), टखने की चोट, और गलत सख्त जूते या हाई हील पहनना भी एड़ी दर्द के बड़े कारण हैं।</p>

<h2>प्लांटर फैसिआइटिस (Plantar Fasciitis) क्या है</h2>
<p>हमारी एड़ी की हड्डी से लेकर पैर की उंगलियों तक एक मोटा, रेशेदार लिगामेंट फैला होता है जिसे "प्लांटर फेशिया" (Plantar Fascia) कहते हैं। यह पैर के आर्च (मेहराब) को सहारा देता है और चलते समय स्प्रिंग की तरह काम करता है। जब इस ऊतक पर बार-बार खिंचाव या अत्यधिक दबाव पड़ता है, तो इसमें छोटे-छोटे सूक्ष्म घाव (Micro-tears) और सूजन आ जाती है — इसी स्थिति को <strong>प्लांटर फैसिआइटिस</strong> कहते हैं।</p>
<p>यह समस्या उन लोगों में ज्यादा होती है जो घंटों खड़े रहकर काम करते हैं, कठोर सतह पर दौड़ते हैं, जिनका वजन अधिक है, या जो बिना सपोर्ट वाले सपाट जूते-चप्पल पहनते हैं। अच्छी बात यह है कि सही देखभाल, स्ट्रेचिंग और उपयुक्त फुटवियर से ज्यादातर मरीज बिना सर्जरी के ठीक हो जाते हैं।</p>

<h2>सुबह उठते ही एड़ी में दर्द क्यों होता है</h2>
<p>यह एड़ी दर्द का सबसे विशिष्ट लक्षण है। रात भर आराम करने के दौरान प्लांटर फेशिया सिकुड़कर छोटी हो जाती है और उसकी सूजन एक जगह जम जाती है। सुबह जैसे ही आप पहला कदम रखते हैं, यह अकड़ी हुई फेशिया अचानक खिंचती है, जिससे एड़ी में तेज सुई चुभने जैसा दर्द होता है। कुछ कदम चलने के बाद जब फेशिया गर्म होकर लचीली हो जाती है, तो दर्द कम हो जाता है।</p>
<p>यही अनुभव देर तक बैठे रहने के बाद या दोपहर में आराम करने के बाद उठने पर भी होता है। अगर आपको हर सुबह पहला कदम रखते ही एड़ी में दर्द होता है, तो यह प्लांटर फैसिआइटिस का सबसे स्पष्ट संकेत है और इसे नजरअंदाज नहीं करना चाहिए।</p>

<h2>एड़ी में दर्द किस विटामिन की कमी से होता है</h2>
<p>एड़ी और तलवों के दर्द का एक अहम कारण <strong>विटामिन D की कमी</strong> है। विटामिन D हड्डियों में कैल्शियम को सोखने में मदद करता है; इसकी कमी से हड्डियां और मांसपेशियां कमजोर हो जाती हैं और अक्सर दोनों एड़ियों तथा पैरों में दर्द व थकान रहती है। इसके साथ <strong>कैल्शियम की कमी</strong> भी हड्डियों को कमजोर करके एड़ी दर्द बढ़ा सकती है।</p>
<p>धूप में पर्याप्त समय न बिताने वाले, बुजुर्ग और घर के अंदर रहने वाले लोगों में विटामिन D की कमी आम है। एक साधारण ब्लड टेस्ट से इसका पता चल जाता है। यदि जांच में कमी निकले, तो डॉक्टर की सलाह पर विटामिन D और कैल्शियम की खुराक तथा धूप, दूध, दही और हरी सब्जियां लेने से राहत मिलती है। ध्यान रहे — बिना जांच के खुद से हाई-डोज विटामिन D न लें।</p>

<h2>एड़ी में दर्द का घरेलू इलाज</h2>
<p>शुरुआती और हल्के एड़ी दर्द में ये आजमाए हुए घरेलू उपाय बहुत राहत देते हैं। हालांकि ये उपाय डॉक्टर के इलाज का विकल्प नहीं हैं, लेकिन इनसे सूजन और दर्द को काफी कम किया जा सकता है:</p>
<ul>
<li><strong>बर्फ की सिकाई (Ice Pack):</strong> एक पतले कपड़े में बर्फ लपेटकर एड़ी पर दिन में 2–3 बार, 15–20 मिनट सिकाई करें। यह सूजन और दर्द दोनों घटाता है। (बर्फ सीधे त्वचा पर न लगाएं।)</li>
<li><strong>स्ट्रेचिंग व्यायाम (Stretching):</strong> सुबह बिस्तर से उतरने से पहले पंजे और पिंडली की हल्की स्ट्रेचिंग करें। दीवार का सहारा लेकर पिंडली खींचना और एक बोतल/रोलर को पैर के तलवे के नीचे घुमाना बहुत फायदेमंद है।</li>
<li><strong>एप्सम सॉल्ट (Epsom Salt) से सिकाई:</strong> गुनगुने पानी में एप्सम सॉल्ट डालकर 15–20 मिनट पैर डुबोकर रखें। इससे मांसपेशियों को आराम मिलता है और अकड़न कम होती है।</li>
<li><strong>सही फुटवियर पहनें:</strong> मुलायम कुशन और अच्छे आर्च सपोर्ट वाले जूते पहनें। सपाट चप्पल, घिसे हुए जूते और हाई हील से बचें। जरूरत हो तो सिलिकॉन हील पैड या इनसोल का उपयोग करें।</li>
<li><strong>आराम और वजन नियंत्रण:</strong> दर्द वाले दिनों में ज्यादा दौड़ने-कूदने से बचें और शरीर का वजन संतुलित रखें, क्योंकि अतिरिक्त वजन एड़ी पर सीधा दबाव डालता है।</li>
</ul>

<h2>एड़ी में दर्द के लिए क्या करें</h2>
<p>अगर आपको एड़ी में दर्द हो रहा है, तो घबराने के बजाय एक व्यवस्थित तरीके से इसका ध्यान रखें। सबसे पहले पैर को आराम दें और ऊपर बताए घरेलू उपाय (बर्फ, स्ट्रेचिंग, सही जूते) अपनाएं। इसके साथ इन बातों का ध्यान रखें:</p>
<ul>
<li>दर्द वाली गतिविधियों (लंबी दौड़, कठोर सतह पर व्यायाम) से कुछ दिन ब्रेक लें।</li>
<li>रोज सुबह-शाम पैर के तलवे और पिंडली की स्ट्रेचिंग को आदत बनाएं।</li>
<li>नंगे पैर सख्त फर्श पर ज्यादा न चलें; घर में भी सपोर्ट वाली चप्पल पहनें।</li>
<li>अगर दर्द तेज है, तो डॉक्टर की सलाह पर ही सूजन-रोधी दवा (जैसे पैरासिटामोल/इबुप्रोफेन) लें — खुद से लंबे समय तक दर्दनिवारक न खाएं।</li>
<li>2–3 हफ्ते के घरेलू उपायों के बाद भी आराम न मिले, तो हड्डी रोग विशेषज्ञ से जांच करवाएं। वे फिजियोथेरेपी, सही इनसोल या जरूरत पड़ने पर अन्य इलाज की सलाह देंगे।</li>
</ul>

