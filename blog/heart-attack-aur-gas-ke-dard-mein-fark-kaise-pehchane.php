<h2>हार्ट अटैक के अन्य असामान्य लक्षण</h2>
<p>हार्ट अटैक हमेशा सीने में तेज दर्द के साथ नहीं आता। कई बार, विशेष रूप से महिलाओं, बुजुर्गों, और मधुमेह के रोगियों में, लक्षण असामान्य होते हैं:</p>
<ul>
<li>केवल बाएं कंधे या हाथ में दर्द</li>
<li>जबड़े या गर्दन में दर्द</li>
<li>पेट के ऊपरी हिस्से में दर्द (आमतौर पर गैस समझा जाता है)</li>
<li>अचानक थकान और कमजोरी</li>
<li>असामान्य रूप से पसीना आना</li>
<li>सिर घूमना या बेहोशी</li>
</ul>

<p>महिलाओं में हार्ट अटैक के लक्षण अक्सर पुरुषों से अलग होते हैं। महिलाओं में हार्ट अटैक के बारे में विस्तार से जानने के लिए पढ़ें <a href="can-anxiety-cause-heart-attack">"क्या चिंता हार्ट अटैक का कारण बन सकती है?"</a>।</p>

<h2>गैस का दर्द समझकर हार्ट अटैक को नजरअंदाज करने के खतरे</h2>
<p>बहुत से लोग, विशेषकर मध्यम आयु वर्ग के, सीने में दर्द होने पर उसे गैस की समस्या समझकर घरेलू उपचार करते रहते हैं। इससे जो खतरे पैदा होते हैं:</p>
<ul>
<li><strong>समय बर्बाद होता है:</strong> हार्ट अटैक में हर मिनट मायने रखता है। "Golden Hour" (पहला एक घंटा) सबसे महत्वपूर्ण होता है।</li>
<li><strong>हृदय की मांसपेशियों को नुकसान:</strong> देरी होने पर हृदय की मांसपेशियां मरती जाती हैं और क्षति स्थायी हो जाती है।</li>
<li><strong>जीवन को खतरा:</strong> देरी से इलाज शुरू होने पर मृत्यु का खतरा कई गुना बढ़ जाता है।</li>
<li><strong>दीर्घकालिक जटिलताएं:</strong> हृदय की कार्यक्षमता कम हो सकती है, जो भविष्य में हृदय विफलता का कारण बन सकती है।</li>
</ul>

<h2>क्या करें अगर सीने में दर्द हो?</h2>
<p>अगर आपको या आपके किसी परिचित को सीने में अचानक दर्द हो, तो ये कदम उठाएं:</p>
<ol>
<li><strong>शांत रहें और तुरंत बैठ जाएं या लेट जाएं।</strong></li>
<li><strong>तुरंत 108 या अपने नजदीकी अस्पताल को कॉल करें।</strong> Sankalp Hospital की 24/7 <a href="emergency-hospital-in-ambikapur">आपातकालीन सेवा</a> से संपर्क करें।</li>
<li><strong>एस्पिरिन की एक गोली (300 mg) चबाकर खाएं</strong> (अगर आपको एस्पिरिन से एलर्जी नहीं है)। यह खून को पतला कर हार्ट अटैक के प्रभाव को कम कर सकता है।</li>
<li><strong>अगर पहले से नाइट्रोग्लिसरीन (NTG) की दवा चल रही हो, तो एक गोली जीभ के नीचे रखें।</strong></li>
<li><strong>मरीज को अकेला न छोड़ें।</strong> उनके साथ रहें और उनकी सांस और नब्ज पर नजर रखें।</li>
<li><strong>यदि मरीज बेहोश हो जाए और सांस न आ रही हो,</strong> तो तुरंत CPR (Cardiopulmonary Resuscitation) शुरू करें।</li>
</ol>

<h2>हार्ट अटैक का निदान</h2>
<p>Sankalp Hospital में <a href="emergency-hospital-in-ambikapur">आपातकालीन विभाग</a> में हार्ट अटैक की पहचान के लिए ये जांच की जाती हैं:</p>
<ul>
<li><strong>ईसीजी (ECG):</strong> सबसे पहली और सबसे महत्वपूर्ण जांच। हार्ट अटैक के विशिष्ट परिवर्तन ईसीजी पर दिखते हैं।</li>
<li><strong>ट्रोपोनिन टेस्ट:</strong> रक्त में ट्रोपोनिन प्रोटीन की मात्रा हार्ट अटैक की पुष्टि करती है।</li>
<li><strong>इकोकार्डियोग्राफी (ECHO):</strong> हृदय की पंपिंग क्षमता और क्षतिग्रस्त क्षेत्र की पहचान।</li>
<li><strong>एंजियोग्राफी:</strong> हृदय की धमनियों में अवरोध की सटीक जगह और गंभीरता का पता लगाने के लिए।</li>
</ul>

<h2>हार्ट अटैक का त्वरित उपचार</h2>
<p>अस्पताल पहुंचने के बाद हार्ट अटैक का इलाज जितनी जल्दी शुरू हो, उतना अच्छा:</p>
<ul>
<li><strong>थ्रोम्बोलाइटिक थेरेपी:</strong> खून का थक्का घोलने वाली दवाइयां (Golden Hour के भीतर सबसे प्रभावी)।</li>
<li><strong>प्राइमरी एंजियोप्लास्टी (Angioplasty):</strong> कैथेटर के माध्यम से अवरोधित धमनी को खोलना और स्टेंट लगाना। यह सबसे प्रभावी उपचार है।</li>
<li><strong>बाईपास सर्जरी:</strong> गंभीर मामलों में सीने की हड्डी काटकर रक्त वाहिकाओं को बदलना।</li>
</ul>

