<h2>हार्ट अटैक क्या है?</h2>
<p>हार्ट अटैक (Myocardial Infarction) तब होता है जब हृदय की मांसपेशियों को रक्त पहुंचाने वाली धमनियों (Coronary Arteries) में अवरोध आ जाता है। यह अवरोध आमतौर पर धमनियों में वसा, कोलेस्ट्रॉल और अन्य पदार्थों के जमा होने (Atherosclerosis) के कारण होता है। जब रक्त प्रवाह पूरी तरह बंद हो जाता है, तो हृदय की मांसपेशियों को ऑक्सीजन नहीं मिल पाती और वे मरने लगती हैं।</p>

<p>हार्ट अटैक के विषय में विस्तृत जानकारी के लिए पढ़ें हमारा लेख <a href="heart-attack-vs-cardiac-arrest">"हार्ट अटैक और कार्डियक अरेस्ट में अंतर"</a>।</p>

<h2>गैस का दर्द क्या है?</h2>
<p>गैस का दर्द (Gas Pain या Flatulence) पेट और आंतों में गैस बनने के कारण होता है। यह आमतौर पर हानिरहित होता है और कुछ समय में अपने आप ठीक हो जाता है। गैस बनने के कई कारण हो सकते हैं जैसे भारी भोजन, कुछ विशेष खाद्य पदार्थ, पाचन संबंधी समस्याएं, या तनाव।</p>

<h2>हार्ट अटैक और गैस के दर्द के बीच 7 प्रमुख अंतर</h2>

<h3>1. दर्द की प्रकृति और तीव्रता</h3>
<p><strong>हार्ट अटैक:</strong> इसमें आमतौर पर सीने में भारीपन, दबाव, जकड़न, या जलन महसूस होती है। कई मरीज इसे "सीने पर कोई भारी वस्तु रख दी हो" या "सीना कुचला जा रहा है" जैसा बताते हैं। दर्द तीव्र और लगातार बढ़ता रहता है।</p>
<p><strong>गैस का दर्द:</strong> यह आमतौर पर ऐंठन, फुलाव, या मरोड़ जैसा महसूस होता है। यह आता-जाता रहता है और कभी-कभी डकार लेने या गैस पास होने से राहत मिलती है।</p>

<h3>2. दर्द का स्थान</h3>
<p><strong>हार्ट अटैक:</strong> दर्द आमतौर पर सीने के बीच में या बाईं ओर शुरू होता है। यह बाएं कंधे, बाएं हाथ, जबड़े, गर्दन, पीठ, या पेट के ऊपरी हिस्से में फैल सकता है। कई बार दर्द केवल बाजू या जबड़े में ही महसूस होता है।</p>
<p><strong>गैस का दर्द:</strong> यह पेट के ऊपरी हिस्से (Epigastric region), छाती के निचले हिस्से, या पसलियों के नीचे महसूस होता है। दर्द आमतौर पर एक जगह सीमित रहता है।</p>

<h3>3. दर्द की अवधि</h3>
<p><strong>हार्ट अटैक:</strong> दर्द आमतौर पर 15-20 मिनट से अधिक समय तक रहता है और बढ़ता जाता है। यह आराम करने या दवा लेने से भी कम नहीं होता।</p>
<p><strong>गैस का दर्द:</strong> यह कुछ मिनटों से लेकर कुछ घंटों तक रह सकता है, लेकिन गैस पास होने या डकार आने से आराम मिल जाता है।</p>

<h3>4. साथ में मौजूद अन्य लक्षण</h3>
<p><strong>हार्ट अटैक के साथ ये लक्षण भी हो सकते हैं:</strong></p>
<ul>
<li>पसीना आना (विशेष रूप से ठंडा पसीना)</li>
<li>सांस लेने में कठिनाई या सांस फूलना</li>
<li>चक्कर आना या बेचैनी</li>
<li>जी मिचलाना या उल्टी</li>
<li>मौत का डर (Sense of Doom)</li>
<li>हाथ-पैर ठंडे पड़ना</li>
</ul>

<p><strong>गैस के दर्द के साथ ये लक्षण होते हैं:</strong></p>
<ul>
<li>पेट फूलना (Bloating)</li>
<li>डकार आना</li>
<li>गैस पास होना</li>
<li>पेट में गड़गड़ाहट</li>
<li>भोजन के प्रति अरुचि</li>
</ul>

<h3>5. दर्द शुरू होने का समय और संदर्भ</h3>
<p><strong>हार्ट अटैक:</strong> यह किसी भी समय हो सकता है - आराम करते हुए, सोते हुए, या शारीरिक परिश्रम के दौरान। कई बार सुबह के समय हार्ट अटैक का खतरा अधिक होता है।</p>
<p><strong>गैस का दर्द:</strong> यह अक्सर भोजन के बाद, विशेष रूप से भारी या तला हुआ भोजन खाने के बाद होता है। कुछ खाद्य पदार्थ जैसे दाल, बीन्स, गोभी, और कार्बोनेटेड ड्रिंक्स गैस बढ़ा सकते हैं।</p>

<h3>6. जोखिम कारक</h3>
<p><strong>हार्ट अटैक के जोखिम कारक:</strong></p>
<ul>
<li>40 वर्ष से अधिक उम्र (पुरुषों में)</li>
<li>50 वर्ष से अधिक उम्र (महिलाओं में)</li>
<li>उच्च रक्तचाप (Hypertension)</li>
<li>उच्च कोलेस्ट्रॉल</li>
<li>मधुमेह (Diabetes)</li>
<li>धूम्रपान और शराब</li>
<li>पारिवारिक इतिहास</li>
<li>मोटापा और गतिहीन जीवनशैली</li>
</ul>

