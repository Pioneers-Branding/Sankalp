<li>तनाव और चिंता</li>
</ul>

<h3>7. दर्द पर प्रतिक्रिया</h3>
<p><strong>हार्ट अटैक:</strong> आराम करने, गहरी सांस लेने, या एंटासिड लेने से भी दर्द कम नहीं होता। नाइट्रोग्लिसरीन (NTG) की गोली कुछ हद तक राहत दे सकती है, लेकिन दर्द पूरी तरह नहीं रुकता।</p>
<p><strong>गैस का दर्द:</strong> एंटासिड, डकार लेने, गुनगुना पानी पीने, या हल्की मालिश से आराम मिल जाता है।</p>

