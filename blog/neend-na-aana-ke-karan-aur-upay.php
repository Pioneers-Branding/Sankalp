<h2>नींद न आने पर कौन सी दवा लें?</h2>
<p>बहुत से लोग जानना चाहते हैं कि <strong>नींद न आने पर कौन सी दवा</strong> ली जाए। यहाँ सबसे ज़रूरी बात यह है — <strong>नींद की गोली (Sleeping Pills) कभी भी अपने मन से या मेडिकल स्टोर से लेकर शुरू न करें।</strong> ये दवाएँ जल्दी आदत (Dependence) डाल देती हैं, याददाश्त और संतुलन पर असर डालती हैं, और बुज़ुर्गों में गिरने का खतरा बढ़ाती हैं। सुरक्षित तरीका यह है:</p>
<ul>
<li>पहले स्लीप हाइजीन और घरेलू उपाय कम से कम 2-3 हफ्ते अपनाएँ।</li>
<li>अगर फिर भी नींद न आए, तो डॉक्टर से मिलें — वे असली कारण (तनाव, थायरॉयड, डिप्रेशन, स्लीप एपनिया) की जाँच करेंगे।</li>
<li>ज़रूरत होने पर डॉक्टर थोड़े समय के लिए ही, सही खुराक में दवा देते हैं, और साथ में <strong>CBT-I (नींद के लिए व्यवहार चिकित्सा)</strong> जैसे सुरक्षित तरीके सुझाते हैं।</li>
<li>पहले से ली जा रही किसी दवा को डॉक्टर की सलाह के बिना अचानक बंद न करें।</li>
</ul>

