<h2>पैर सुन्न होने के कारण</h2>
<p>पैरों में सुन्नपन कोई अकेली बीमारी नहीं, बल्कि कई अंदरूनी वजहों का लक्षण है। सही इलाज के लिए असली कारण पहचानना जरूरी है:</p>

<h3>1. नसों पर दबाव (एक ही मुद्रा में बैठना)</h3>
<p>सबसे आम और हानिरहित कारण किसी नस पर अस्थायी दबाव है। पालथी मारकर, पैर के ऊपर पैर रखकर या घंटों एक ही मुद्रा में बैठे रहने से पैर की नस और रक्त वाहिका दब जाती है, जिससे पैर सुन्न हो जाता है। मुद्रा बदलते ही खून का बहाव सामान्य होता है और झनझनाहट के साथ सुन्नपन धीरे-धीरे ठीक हो जाता है। इसी तरह की समस्या के बारे में और जानने के लिए हमारा लेख <a href="/blog/pair-me-jhunjhuni-hona">पैर में झुनझुनी होना</a> जरूर पढ़ें।</p>

<h3>2. डायबिटिक न्यूरोपैथी (शुगर से नसों की कमजोरी)</h3>
<p>लंबे समय तक हाई ब्लड शुगर रहने से पैरों की नसें धीरे-धीरे कमजोर और क्षतिग्रस्त हो जाती हैं, जिसे 'डायबिटिक न्यूरोपैथी' कहते हैं। इसमें आमतौर पर दोनों पैरों के तलवों और उंगलियों से शुरू होकर सुन्नपन, जलन और झनझनाहट ऊपर की ओर बढ़ती है। डायबिटीज के मरीजों में यह सुन्नपन का एक बहुत बड़ा और गंभीर कारण है, क्योंकि इससे पैर की चोट या घाव का पता ही नहीं चल पाता।</p>

<h3>3. विटामिन B12 की कमी</h3>
<p>विटामिन B12 हमारी नसों की सुरक्षा परत (माइलिन) को स्वस्थ रखता है। शाकाहारी भोजन करने वालों, बुजुर्गों और पेट की समस्या वाले लोगों में इसकी कमी आम है। B12 की कमी से हाथ-पैर में सुन्नपन, झनझनाहट, कमजोरी और चलते समय संतुलन बिगड़ने जैसी दिक्कतें होती हैं। यह सुन्नपन के सबसे आसानी से ठीक होने वाले कारणों में से एक है।</p>

<h3>4. खराब रक्त संचार (PAD)</h3>
<p>जब पैरों तक खून पहुंचाने वाली धमनियां सिकुड़ या ब्लॉक हो जाती हैं, तो इसे 'पेरिफेरल आर्टरी डिजीज (PAD)' कहते हैं। पर्याप्त खून और ऑक्सीजन न मिलने से पैर ठंडे पड़ जाते हैं, सुन्न हो जाते हैं और चलने पर पिंडली में दर्द होता है। धूम्रपान करने वालों, डायबिटीज और हाई कोलेस्ट्रॉल के मरीजों में यह जोखिम ज्यादा होता है।</p>

<h3>5. स्लिप डिस्क / कमर की नस दबना (Sciatica)</h3>
<p>कमर की रीढ़ की हड्डी में डिस्क खिसकने (स्लिप डिस्क) या नस दबने से 'साइटिका' की समस्या होती है। इसमें कमर से लेकर कूल्हे, जांघ और पैर तक दर्द के साथ सुन्नपन और झनझनाहट फैलती है, जो अक्सर एक ही पैर में महसूस होती है। नस पर दबाव से जुड़ी समस्या के लिए हमारा लेख <a href="/blog/pair-ki-nas-chadhna-ke-karan-aur-upay">पैर की नस चढ़ना के कारण और उपाय</a> भी पढ़ें।</p>

<h3>6. थायरॉयड की समस्या (Hypothyroidism)</h3>
<p>थायरॉयड ग्रंथि के कम सक्रिय (हाइपोथायरॉयडिज्म) होने पर शरीर में तरल जमा होकर नसों पर दबाव डालता है, जिससे हाथ-पैर में सुन्नपन और झनझनाहट हो सकती है। इसके साथ थकान, वजन बढ़ना और त्वचा का रूखापन भी दिखता है। एक साधारण ब्लड टेस्ट से इसका पता लगाया जा सकता है।</p>

<h3>7. अन्य कारण</h3>
<p>कभी-कभी पैरों में सुन्नपन के साथ जलन भी महसूस होती है, जो नस की कमजोरी का संकेत है — इस बारे में विस्तार से हमारा लेख <a href="/blog/pair-me-jalan">पैरों में जलन के कारण और उपाय</a> पढ़ें। इसके अलावा शराब का अधिक सेवन, कुछ दवाओं के साइड इफेक्ट, गुर्दे की बीमारी और लंबे समय तक टाइट जूते पहनना भी सुन्नपन के कारण बन सकते हैं।</p>

<h2>लक्षण</h2>
<p>पैर सुन्न होने के साथ अक्सर ये लक्षण भी महसूस होते हैं:</p>
<ul>
<li>पैर या तलवे में छूने का एहसास कम होना या बिल्कुल न होना।</li>
<li>सुई चुभने, झनझनाहट या 'करंट' जैसा महसूस होना।</li>
<li>पैरों में कमजोरी और भारीपन, चलने पर लड़खड़ाहट।</li>
<li>तलवों में जलन, खासकर रात के समय।</li>
<li>पैर ठंडे पड़ना या रंग में बदलाव (खराब रक्त संचार में)।</li>
<li>गर्म-ठंडे का एहसास कम होना, जिससे चोट का पता देर से चलना।</li>
</ul>

<h2>जांच (Diagnosis)</h2>
<p>सुन्नपन के असली कारण का पता लगाने के लिए डॉक्टर निम्नलिखित जांच कर सकते हैं:</p>
<ul>
<li><strong>शारीरिक और न्यूरोलॉजिकल जांच:</strong> डॉक्टर पैर की संवेदना, ताकत और रिफ्लेक्स की जांच करते हैं।</li>
<li><strong>ब्लड टेस्ट:</strong> ब्लड शुगर (HbA1c), विटामिन B12 लेवल, थायरॉयड (TSH) और किडनी फंक्शन की जांच।</li>
<li><strong>नर्व कंडक्शन टेस्ट (NCV/EMG):</strong> नसों में सिग्नल की गति और उनकी कमजोरी जांचने के लिए।</li>
<li><strong>MRI / एक्स-रे:</strong> कमर की स्लिप डिस्क या नस दबने की पुष्टि के लिए।</li>
<li><strong>डॉपलर अल्ट्रासाउंड:</strong> पैरों में रक्त संचार और धमनियों में रुकावट (PAD) जांचने के लिए।</li>
</ul>

<h2>घरेलू उपाय</h2>
<p>शुरुआती और हल्के सुन्नपन में ये घरेलू उपाय बहुत असरदार हैं। ध्यान रहे — ये उपाय डॉक्टरी इलाज के सहायक हैं, विकल्प नहीं:</p>

<h3>1. गुनगुने पानी से सिकाई और मालिश</h3>
<p>गुनगुने पानी में पैर भिगोने या गुनगुने पानी की सिकाई से रक्त संचार बढ़ता है और नसों को आराम मिलता है। सरसों या नारियल के गुनगुने तेल से तलवों और पिंडली की हल्के हाथों से मालिश करने पर सुन्नपन में जल्दी राहत मिलती है। (डायबिटीज के मरीज पानी का तापमान हाथ से जांच लें)।</p>

<h3>2. हल्की एक्सरसाइज और स्ट्रेचिंग</h3>
<p>रोजाना हल्की एक्सरसाइज, पैदल चलना और पैर की स्ट्रेचिंग करने से नसों और मांसपेशियों में खून का बहाव बेहतर होता है। पंजे को ऊपर-नीचे करना, टखने को गोल घुमाना और पिंडली की स्ट्रेचिंग सुन्नपन कम करने में बहुत मदद करती है। एक ही जगह घंटों बैठने से बचें।</p>

<h3>3. विटामिन B12 युक्त आहार</h3>
<p>अपने भोजन में विटामिन B12 और आयरन से भरपूर चीजें शामिल करें, जैसे दूध, दही, पनीर, अंडा, हरी पत्तेदार सब्जियां और साबुत अनाज। शाकाहारी लोगों को डॉक्टर की सलाह से B12 सप्लीमेंट लेने की जरूरत पड़ सकती है, क्योंकि यह नसों को स्वस्थ रखने के लिए जरूरी है।</p>

<h3>4. ब्लड शुगर कंट्रोल</h3>
<p>अगर आपको डायबिटीज है, तो ब्लड शुगर को नियंत्रण में रखना सुन्नपन रोकने का सबसे जरूरी कदम है। संतुलित आहार, नियमित व्यायाम और समय पर दवा से शुगर कंट्रोल रखें, और रोजाना अपने पैरों की जांच करें कि कहीं कोई कटाव या घाव तो नहीं है।</p>

<h3>5. मुद्रा बदलना और सही जीवनशैली</h3>
<p>पालथी मारकर या पैर के ऊपर पैर रखकर लंबे समय तक न बैठें। हर 30-40 मिनट में मुद्रा बदलें और थोड़ा टहलें। आरामदायक और सही नाप के जूते पहनें, धूम्रपान व शराब से बचें, और शरीर को हाइड्रेट रखें — यह नसों और रक्त संचार दोनों के लिए फायदेमंद है।</p>

