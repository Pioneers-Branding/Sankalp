<h2>कमर दर्द के प्रकार और उनके उपचार</h2>

<h3>तीव्र कमर दर्द (Acute Back Pain)</h3>
<p>यह अचानक शुरू होता है और आमतौर पर कुछ दिनों से लेकर 6 सप्ताह तक रहता है। इसमें आराम, बर्फ या गर्म सिंकाई, हल्के दर्द निवारक, और धीरे-धीरे सामान्य गतिविधियां शुरू करना सबसे कारगर उपचार है।</p>

<h3>उप-तीव्र कमर दर्द (Sub-Acute Back Pain)</h3>
<p>यह 6-12 सप्ताह तक रहता है। इसमें फिजियोथेरेपी, मांसपेशियों को मजबूत करने वाले व्यायाम, और कभी-कभी मांसपेशियों को आराम देने वाली दवाइयां दी जाती हैं।</p>

<h3>दीर्घकालिक कमर दर्द (Chronic Back Pain)</h3>
<p>12 सप्ताह से अधिक समय तक बना रहने वाला दर्द। इसमें विशेषज्ञ की देखरेख में दवाइयां, फिजियोथेरेपी, इंजेक्शन, और जीवनशैली में बदलाव की जरूरत होती है।</p>

