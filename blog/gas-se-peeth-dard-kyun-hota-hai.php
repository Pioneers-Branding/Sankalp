    <h2>अक्सर पूछे जाने वाले प्रश्न (FAQs)</h2>

    <div class="faq-item">
      <h4>क्या गैस की वजह से पीठ में दर्द होता है?</h4>
      <p>हां, गैस की वजह से पीठ में दर्द हो सकता है। जब आंतों में अधिक गैस जमा होती है, तो यह पेट के चारों ओर दबाव बनाती है जो पीठ और कमर तक फैल सकता है। पेट और पीठ की नसें आपस में जुड़ी होती हैं, इसलिए पेट की गैस का दर्द पीठ में महसूस होता है।</p>
    </div>

    <div class="faq-item">
      <h4>पीठ में गैस के लक्षण क्या हैं?</h4>
      <p>पीठ में गैस के मुख्य लक्षण हैं — खाने के बाद पीठ दर्द बढ़ना, पेट फूलना और भारीपन, डकार या गैस निकलने पर दर्द कम होना, दर्द का पीठ-पेट-कमर में जगह बदलना, और कब्ज। अगर ये लक्षण साथ में हों, तो पीठ दर्द का कारण गैस होने की संभावना अधिक है।</p>
    </div>

    <div class="faq-item">
      <h4>पीठ में गैस बने तो क्या करें?</h4>
      <p>पीठ में गैस बने तो गुनगुना पानी पिएं, एक चुटकी हींग गुनगुने पानी में घोलकर लें, 10-15 मिनट टहलें, और पवनमुक्तासन करें (पीठ के बल लेटकर घुटनों को छाती से लगाएं)। पीठ पर गर्म सेक भी तुरंत राहत देता है। अगर 2-3 दिन में आराम न मिले तो डॉक्टर से मिलें।</p>
    </div>

    <div class="faq-item">
      <h4>पीठ में गैस फंस जाए तो क्या करें?</h4>
      <p>पीठ में गैस फंस जाए तो सबसे पहले पवनमुक्तासन करें — पीठ के बल लेटकर दोनों घुटनों को छाती से लगाकर 30-60 सेकंड रोकें। फिर 10-15 मिनट धीमी सैर करें और गुनगुने पानी में हींग या अजवाइन डालकर पिएं। खाने के तुरंत बाद लेटने से बचें, इससे गैस आंत में फंसी रहती है।</p>
    </div>

    <div class="faq-item">
      <h4>गैस के कारण पीठ में दर्द का इलाज क्या है?</h4>
      <p>गैस के कारण पीठ में दर्द का इलाज कारण पर निर्भर करता है। शुरुआत में घरेलू उपाय (हींग, गुनगुना पानी, सैर, पवनमुक्तासन) आजमाएं। आराम न मिलने पर डॉक्टर की सलाह से एंटासिड या गैस की दवा लें। बार-बार समस्या होने पर गैस्ट्रोएंटरोलॉजिस्ट से जांच कराएं — GERD, IBS या कब्ज जैसी जड़ की समस्या का इलाज होने पर पीठ दर्द भी ठीक हो जाता है।</p>
    </div>

    <div class="faq-item">
      <h4>गैस के कारण कमर में दर्द क्यों होता है?</h4>
      <p>जब गैस बड़ी आंत के निचले हिस्से या मोड़ में फंस जाती है, तो उसका दबाव कमर (निचली पीठ) की नसों और मांसपेशियों पर पड़ता है, जिससे कमर में दर्द होता है। कब्ज और लंबे समय तक बैठे रहना इसे और बढ़ाते हैं। कमर दर्द के साथ पेट फूलना और डकार होना गैस का संकेत है।</p>
    </div>

    <div class="faq-item">
      <h4>गैस से पीठ दर्द का दर्द कैसा महसूस होता है?</h4>
      <p>गैस से होने वाला पीठ दर्द आमतौर पर सुस्त और दबाव वाला दर्द होता है। यह पेट में भारीपन के साथ होता है और खाने के बाद बढ़ता है। कभी-कभी तेज दर्द भी हो सकता है जो आता-जाता रहता है।</p>
    </div>

