<h2>Important Precautions and Side Effects</h2>
<div class="alert alert-warning mt-3 mb-4 shadow-sm border-0" style="background-color: #fff3cd; border-left: 5px solid #ffc107 !important;">
  <strong>Consult Your Doctor First:</strong> Before adding Moringa to your routine, always discuss with your healthcare provider, especially if you're on diabetes medications like insulin, metformin, or sulfonylureas. Moringa may enhance the effects of these medications, leading to hypoglycemia (low blood sugar).
</div>

<h3>Possible Side Effects:</h3>
<ul>
  <li><strong>Digestive Issues:</strong> Some people may experience stomach upset, diarrhea, or nausea (usually resolves with lower doses)</li>
  <li><strong>Interactions with Medications:</strong> Moringa may interact with blood-thinning medications, blood pressure medications, and diabetes drugs</li>
  <li><strong>Allergic Reactions:</strong> Rare but possible - discontinue use if you experience itching, swelling, or breathing difficulties</li>
  <li><strong>Hypoglycemia Risk:</strong> When combined with diabetes medications, blood sugar may drop too low</li>
</ul>

