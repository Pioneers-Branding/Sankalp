<h2>एड़ी फटने के कारण (Causes of Cracked Heels)</h2>
<p>एड़ी की त्वचा हमारे शरीर के बाकी हिस्सों से ज्यादा मोटी और सख्त होती है, और उस पर सबसे ज्यादा दबाव पड़ता है। जब यह त्वचा नमी खोकर सूख जाती है और लचीलापन कम हो जाता है, तो वह फटने लगती है। इसके पीछे कई कारण हो सकते हैं:</p>

<h3>1. रूखी त्वचा और नमी की कमी</h3>
<p>एड़ी फटने का सबसे आम कारण त्वचा का रूखापन और प्राकृतिक नमी की कमी है। जब एड़ी की त्वचा में मौजूद तेल और नमी कम हो जाती है, तो वह कठोर होकर फटने लगती है और उसमें दरारें पड़ जाती हैं।</p>

<h3>2. ठंडा और शुष्क मौसम</h3>
<p>सर्दियों में हवा में नमी बहुत कम हो जाती है, जिससे त्वचा जल्दी सूखती है। यही वजह है कि ज्यादातर लोगों की एड़ियां खासकर ठंड के मौसम में फटती हैं। गर्म पानी से बार-बार पैर धोना भी त्वचा का प्राकृतिक तेल छीन लेता है।</p>

<h3>3. ज्यादा देर खड़े रहना या नंगे पैर चलना</h3>
<p>घंटों लगातार खड़े रहकर काम करने, सख्त फर्श पर नंगे पैर चलने, या खुली चप्पल पहनने से एड़ी पर लगातार दबाव और घर्षण पड़ता है। इससे त्वचा मोटी होकर फटने लगती है। अगर आपको एड़ी में दर्द भी रहता है, तो हमारा लेख <a href="/blog/edi-me-dard-ke-karan-aur-upay">एड़ी में दर्द के कारण और उपाय</a> जरूर पढ़ें।</p>

<h3>4. मोटापा (ज्यादा वजन)</h3>
<p>शरीर का ज्यादा वजन एड़ी की गद्दी (fat pad) पर अतिरिक्त दबाव डालता है, जिससे एड़ी की त्वचा किनारों से फैलती है और फटने लगती है। ज्यादा वजन वाले लोगों में यह समस्या आम है।</p>

<h3>5. विटामिन और जिंक की कमी</h3>
<p>शरीर में विटामिन E, विटामिन B3 (नियासिन) और जिंक की कमी से त्वचा रूखी, बेजान और कमजोर हो जाती है। पोषक तत्वों की यह कमी एड़ी फटने का एक बड़ा लेकिन नजरअंदाज किया जाने वाला कारण है। संतुलित आहार न लेने वाले लोगों में यह समस्या ज्यादा देखी जाती है।</p>

<h3>6. डायबिटीज (मधुमेह)</h3>
<p>डायबिटीज के मरीजों में हाई ब्लड शुगर के कारण त्वचा में गंभीर सूखापन आ जाता है और नसें कमजोर हो जाती हैं, जिससे पैरों में पसीना और प्राकृतिक नमी बनना कम हो जाता है। ऐसे में एड़ियां जल्दी फटती हैं और उनमें संक्रमण (infection) का खतरा भी बढ़ जाता है, इसलिए डायबिटीज के मरीजों को अपने पैरों की खास देखभाल करनी चाहिए।</p>

<h3>7. थायरॉयड और फंगल इन्फेक्शन</h3>
<p>थायरॉयड हार्मोन के असंतुलन (खासकर हाइपोथायरॉयडिज्म) में त्वचा रूखी हो जाती है और पसीना कम आता है, जिससे एड़ियां फटने लगती हैं। इसके अलावा फंगल इन्फेक्शन यानी एथलीट्स फुट (Athlete's Foot) के कारण भी एड़ी और पंजों की त्वचा फटती और छिलती है। ऐसी स्थिति में <a href="/blog/pair-me-khujli-hona">पैरों में खुजली</a> भी हो सकती है।</p>

<h3>8. गलत फुटवियर</h3>
<p>पीछे से खुले जूते-चप्पल, बहुत सख्त तलवे वाले या सही नाप के न होने वाले फुटवियर एड़ी को खुला और असुरक्षित छोड़ देते हैं, जिससे धूल, रूखापन और घर्षण बढ़ता है और एड़ी फटने लगती है। लगातार खड़े रहने या तलवे में दर्द रहने पर हमारा लेख <a href="/blog/pair-ke-talwe-me-dard">पैर के तलवे में दर्द</a> भी सहायक है।</p>

<h2>एड़ी फटने के लक्षण (Symptoms)</h2>
<ul>
<li>एड़ी की त्वचा का रूखा, मोटा और सख्त हो जाना।</li>
<li>एड़ी के किनारों पर पीली या भूरी परत (कॉलस) जमना।</li>
<li>एड़ी में छोटी या गहरी दरारें (fissures) पड़ना।</li>
<li>चलने या खड़े होने पर एड़ी में दर्द और खिंचाव महसूस होना।</li>
<li>गहरी दरारों से कभी-कभी खून निकलना।</li>
<li>दरारों में खुजली, जलन या लालिमा होना।</li>
</ul>

<h2>जांच (Diagnosis)</h2>
<p>ज्यादातर मामलों में एड़ी फटना एक बाहरी और आम समस्या है, लेकिन बार-बार या गंभीर रूप से फटने पर डॉक्टर अंदरूनी कारण जानने के लिए ये जांच कर सकते हैं:</p>
<ul>
<li><strong>शारीरिक जांच:</strong> डॉक्टर एड़ी की त्वचा, दरारों की गहराई और किसी संक्रमण की जांच करते हैं।</li>
<li><strong>ब्लड टेस्ट:</strong> ब्लड शुगर (डायबिटीज), थायरॉयड और विटामिन की कमी का पता लगाने के लिए।</li>
<li><strong>फंगल टेस्ट:</strong> फंगल इन्फेक्शन (एथलीट्स फुट) का संदेह होने पर त्वचा की जांच।</li>
</ul>

<h2>एड़ी फटने के रामबाण घरेलू उपाय (Home Remedies)</h2>
<p>शुरुआती और सामान्य रूप से फटी एड़ियों के लिए ये घरेलू उपाय बहुत असरदार हैं। ध्यान रहे — ये उपाय नियमित देखभाल के लिए हैं और गंभीर या संक्रमित एड़ियों में डॉक्टरी सलाह जरूरी है:</p>

<h3>1. रात में मॉइस्चराइज़र और मोजे</h3>
<p>सबसे असरदार उपाय है — रात को सोने से पहले एड़ियों पर अच्छा मॉइस्चराइज़र, नारियल तेल या पेट्रोलियम जेली लगाकर सूती मोजे पहन लें। इससे नमी पूरी रात त्वचा में बंद रहती है और सुबह तक एड़ियां मुलायम हो जाती हैं। कुछ ही दिनों में दरारें भरने लगती हैं।</p>

<h3>2. गुनगुने पानी में पैर भिगोकर सफाई</h3>
<p>एक टब में गुनगुना पानी लेकर उसमें 15-20 मिनट पैर भिगोएं। इसके बाद प्यूमिस स्टोन (झांवा पत्थर) से एड़ी की मोटी, मृत त्वचा को हल्के हाथों से रगड़कर साफ करें। ध्यान रहे — त्वचा को जोर से न रगड़ें और तुरंत बाद मॉइस्चराइज़र जरूर लगाएं। (डायबिटीज के मरीज त्वचा को रगड़ने से पहले डॉक्टर से सलाह लें।)</p>

<h3>3. ज्यादा पानी पीना</h3>
<p>शरीर में पानी की कमी भी त्वचा को रूखा बनाती है। दिनभर पर्याप्त मात्रा में पानी पीने से त्वचा अंदर से नम रहती है और एड़ियां जल्दी नहीं फटतीं।</p>

<h3>4. विटामिन युक्त संतुलित आहार</h3>
<p>अपने खाने में विटामिन E, विटामिन B3, जिंक और ओमेगा-3 से भरपूर चीजें जैसे हरी सब्जियां, मेवे, बीज, दूध और मौसमी फल शामिल करें। सही पोषण त्वचा को अंदर से मजबूत और मुलायम बनाता है।</p>

<h3>5. बंद और आरामदायक जूते पहनें</h3>
<p>पीछे से खुली चप्पल की जगह पीछे से बंद, गद्देदार और आरामदायक जूते पहनें। इससे एड़ी धूल, रूखेपन और घर्षण से बची रहती है और दरारें नहीं पड़तीं।</p>

