<h2>पैरों में झनझनाहट क्यों होती है? जानिए मुख्य कारण</h2>
<p>पैर में झनझनाहट के कारण और उपाय समझने के लिए सबसे पहले यह जानना जरूरी है कि झनझनाहट होती क्यों है। पैरों में झनझनाहट और सुन्नपन के पीछे कई न्यूरोलॉजिकल (नसों से जुड़े) और सिस्टमिक (पूरे शरीर को प्रभावित करने वाले) कारण हो सकते हैं। सबसे प्रमुख कारण निम्नलिखित हैं:</p>

<h3>1. डायबिटिक न्यूरोपैथी (Diabetic Neuropathy)</h3>
<p>लगातार पैरों में झनझनाहट का सबसे बड़ा कारण मधुमेह (Diabetes) है। जब रक्त में शुगर का स्तर लंबे समय तक अधिक रहता है, तो यह शरीर की छोटी नसों (Peripheral nerves) को नुकसान पहुंचाता है। इसके कारण पैरों के तलवों से लेकर घुटनों तक जलन, झनझनाहट, दर्द और अंततः सुन्नपन आ जाता है। यदि आपको झनझनाहट के साथ ज्यादा प्यास लगती है या बार-बार पेशाब आता है, तो तुरंत अपना ब्लड शुगर चेक करवाएं।</p>

<h3>2. नसों का दबना (Nerve Compression / Sciatica)</h3>
<p>यदि आपकी कमर के निचले हिस्से (Lower back) में रीढ़ की हड्डी के बीच की डिस्क खिसक गई है (Herniated disc), तो वह वहां से निकलने वाली 'साइटिक नर्व' (Sciatic nerve) पर दबाव डालती है। इसके कारण कमर से लेकर पैरों के नीचे तक एक तेज दर्द और भयंकर झनझनाहट होती है जिसे साइटिका (Sciatica) कहते हैं।</p>

<h3>3. विटामिन B12 की कमी (Vitamin B12 Deficiency)</h3>
<p>विटामिन B12 तंत्रिकाओं (Nerves) के बाहरी सुरक्षा कवच (Myelin sheath) को बनाने और स्वस्थ रखने के लिए अत्यंत आवश्यक है। इसकी कमी होने पर यह कवच टूटने लगता है, जिससे तंत्रिकाएं सही से काम नहीं कर पातीं। इसका सीधा असर सबसे पहले हाथ-पैरों की नसों पर पड़ता है, इसलिए <strong>झनझनाहट किस विटामिन की कमी से होती है</strong> — इसका सबसे आम जवाब विटामिन B12 है। यह कमी शाकाहारी लोगों, बुजुर्गों और लंबे समय तक एसिडिटी की दवा लेने वालों में बहुत आम है।</p>
<p><strong>विटामिन B12 की कमी से क्या होता है (लक्षण):</strong></p>
<ul>
<li>हाथ और पैरों में झनझनाहट, सुन्नपन या सुई चुभने जैसा अहसास।</li>
<li>हर समय अत्यधिक थकान, कमजोरी और चक्कर आना।</li>
<li>याददाश्त कमजोर होना, ध्यान न लगना और चिड़चिड़ापन।</li>
<li>दिन भर सुस्ती और ज्यादा नींद आना।</li>
<li>जीभ में जलन, मुंह के छाले और त्वचा का पीलापन (एनीमिया)।</li>
</ul>
<p>एक साधारण ब्लड टेस्ट से B12 का स्तर पता चल जाता है। समय पर इलाज न होने पर B12 की कमी से होने वाला नसों का नुकसान स्थायी हो सकता है, इसलिए इसे नजरअंदाज न करें।</p>

<h3>4. परिधीय धमनी रोग (Peripheral Artery Disease - PAD)</h3>
<p>यह रक्त संचार से जुड़ी एक बीमारी है जिसमें पैरों तक शुद्ध रक्त ले जाने वाली धमनियों (Arteries) में फैट (प्लाक) जमा हो जाता है और वे संकरी हो जाती हैं। पैरों तक पर्याप्त ऑक्सीजन और रक्त न पहुंचने के कारण उनमें झनझनाहट, ठंडक और चलते समय मांसपेशियों में ऐंठन (Cramps) होती है।</p>

<h3>5. ऑटोइम्यून बीमारियां (Autoimmune Diseases)</h3>
<p>रूमेटाइड अर्थराइटिस (RA), ल्यूपस या मल्टीपल स्केलेरोसिस (MS) जैसी बीमारियों में शरीर का इम्यून सिस्टम गलती से अपनी ही स्वस्थ नसों और ऊतकों पर हमला करने लगता है। इसके कारण नसों में सूजन आ जाती है और पैरों में सुन्नपन व झनझनाहट शुरू हो जाती है।</p>

<h3>6. थायरॉयड विकार (Hypothyroidism)</h3>
<p>जब थायरॉयड ग्रंथि अंडरएक्टिव (कम सक्रिय) होती है, तो शरीर का मेटाबॉलिज्म धीमा हो जाता है। क्रोनिक हाइपोथायरायडिज्म के मामलों में, तरल पदार्थ का जमाव होने से नसों पर दबाव पड़ता है, जिससे परिधीय न्यूरोपैथी (Peripheral neuropathy) विकसित होती है।</p>

<h2>अन्य महत्वपूर्ण कारण</h2>
<ul>
<li><strong>अत्यधिक शराब का सेवन:</strong> लंबे समय तक भारी मात्रा में शराब पीने से नसों में विषाक्तता (Toxicity) फैलती है जिसे अल्कोहलिक न्यूरोपैथी कहते हैं।</li>
<li><strong>दवाइयों के साइड इफेक्ट्स:</strong> कैंसर की कीमोथेरेपी (Chemotherapy), कुछ एंटीबायोटिक्स और हाई ब्लड प्रेशर की दवाइयां नसों पर असर डाल सकती हैं।</li>
<li><strong>किडनी की विफलता (Kidney Failure):</strong> रक्त में यूरिया और अन्य टॉक्सिन्स के जमा होने (यूरेमिया) से भी नसों को नुकसान पहुंचता है।</li>
</ul>

<h2>पैर के तलवे में झनझनाहट या झुनझुनी क्यों होती है?</h2>
<p>बहुत से लोगों को झनझनाहट सबसे पहले <strong>पैर के तलवों</strong> में महसूस होती है — जैसे तलवे पर चींटियां चल रही हों, मोजे पहने जैसा भारीपन हो या रात में तलवों में जलन व झुनझुनी बढ़ जाए। इसका कारण यह है कि शरीर की सबसे लंबी नसें पैर के तलवों तक जाती हैं, इसलिए नस डैमेज (Neuropathy) का असर सबसे पहले तलवों पर ही दिखता है। पैर के तलवे में झनझनाहट के प्रमुख कारण हैं:</p>
<ul>
<li><strong>डायबिटिक न्यूरोपैथी की शुरुआत:</strong> दोनों तलवों में झनझनाहट और जलन — जो रात में बढ़ जाती है — डायबिटीज से नस डैमेज का सबसे पहला संकेत है। यह लक्षण दिखते ही ब्लड शुगर (HbA1c) जरूर चेक करवाएं।</li>
<li><strong>विटामिन B12 की कमी:</strong> तलवों और उंगलियों में सुई चुभने जैसी झुनझुनी B12 की कमी का शुरुआती लक्षण है।</li>
<li><strong>टार्सल टनल सिंड्रोम (Tarsal Tunnel Syndrome):</strong> एड़ी के अंदरूनी हिस्से में नस दबने से तलवे में झनझनाहट और जलन होती है, जो ज्यादा चलने या खड़े रहने पर बढ़ती है।</li>
<li><strong>देर तक खड़े रहना या सख्त फुटवियर:</strong> घंटों खड़े रहकर काम करने या बिना कुशन वाले सख्त जूते-चप्पल पहनने से तलवों की नसों पर दबाव पड़ता है।</li>
</ul>
<p>यदि तलवों की झनझनाहट रोज हो रही है या रात में नींद खराब कर रही है, तो यह केवल थकान नहीं है — ब्लड शुगर और विटामिन B12 की जांच करवाकर न्यूरोलॉजिस्ट से मिलें।</p>

<h2>सिर्फ एक ही पैर (बाएं या दाएं) में झनझनाहट क्यों होती है?</h2>
<p>जब झनझनाहट दोनों पैरों में एक साथ हो, तो कारण अक्सर पूरे शरीर से जुड़ा होता है (जैसे डायबिटीज या विटामिन B12 की कमी)। लेकिन यदि झनझनाहट या सुन्नपन <strong>सिर्फ एक ही पैर में</strong> (जैसे केवल बाएं या दाएं पैर में) हो, तो इसका कारण आमतौर पर स्थानीय (Local) होता है, जैसे:</p>
<ul>
<li><strong>साइटिका / नस का दबना:</strong> रीढ़ की एक तरफ की नस दबने से उसी तरफ के पैर में करंट जैसी झनझनाहट कमर से नीचे तक जाती है।</li>
<li><strong>देर तक पैर मोड़कर बैठना:</strong> पालथी मारकर या पैर पर पैर चढ़ाकर बैठने से एक पैर की नस दब जाती है और वह "सो" जाता है।</li>
<li><strong>चोट या रक्त का थक्का:</strong> किसी एक पैर की नस में चोट या ब्लड क्लॉट (DVT) से भी एकतरफा झनझनाहट व सूजन हो सकती है।</li>
</ul>
<p>यदि एक ही पैर की झनझनाहट के साथ अचानक तेज दर्द, सूजन या कमजोरी हो, तो इसे नजरअंदाज न करें और डॉक्टर से मिलें।</p>

