<ul class="mb-0 text-dark">
<li>झनझनाहट का अचानक शुरू होना और तेजी से पूरी टांगों में फैल जाना।</li>
<li>पैरों में इतनी कमजोरी आना कि चलने में लड़खड़ाहट हो या आप गिर जाएं (लखवा/Paralysis का संकेत)।</li>
<li>मूत्र या मल विसर्जन पर नियंत्रण न रहना (Bowel/Bladder Incontinence)।</li>
<li>झनझनाहट के साथ-साथ बोलने में लड़खड़ाहट या चेहरे का सुन्न होना (यह स्ट्रोक/Stroke का संकेत है, तुरंत इमरजेंसी में जाएं)।</li>
<li>पैरों में किसी घाव या कट का होना, जिसका आपको सुन्नपन के कारण दर्द महसूस न हो।</li>
</ul>
</div>

<h2>डॉक्टर सही कारण का पता कैसे लगाते हैं? (Diagnosis)</h2>
<p>एक विशेषज्ञ न्यूरोलॉजिस्ट या जनरल फिजिशियन सटीक निदान के लिए निम्नलिखित परीक्षणों (Tests) की सलाह दे सकते हैं:</p>
<ul>
<li><strong>नर्व कंडक्शन वेलोसिटी (NCV) और EMG:</strong> यह टेस्ट यह मापता है कि आपकी नसें कितनी तेजी से और कितनी अच्छी तरह इलेक्ट्रिक सिग्नल भेज रही हैं। यह नसों के डैमेज का पता लगाने का सबसे सटीक तरीका है।</li>
<li><strong>रक्त परीक्षण (Blood Tests):</strong> ब्लड शुगर (Fasting, PP, HbA1c), विटामिन B12, थायरॉयड प्रोफाइल (TSH), और किडनी फंक्शन (KFT) की जांच।</li>
<li><strong>एमआरआई (MRI Spine):</strong> यदि डॉक्टर को संदेह है कि झनझनाहट का कारण रीढ़ की हड्डी में दबी हुई नस (Slip disc) है, तो कमर का एमआरआई किया जाता है।</li>
</ul>

<h2>हाथ-पैर में झनझनाहट का इलाज क्या है? (Treatment & Medicine)</h2>
<p>झनझनाहट का इलाज उसके मूल कारण पर निर्भर करता है — यानी नस को नुकसान पहुंचाने वाली असली वजह को ठीक करना ही सबसे प्रभावी इलाज है। इसीलिए बिना जांच के कोई भी दवा खुद से लेना ठीक नहीं है। डॉक्टर आमतौर पर कारण के अनुसार इस तरह इलाज करते हैं:</p>
<ul>
<li><strong>डायबिटीज होने पर:</strong> ब्लड शुगर को सख्ती से कंट्रोल करना सबसे जरूरी है, क्योंकि यही आगे के नस डैमेज को रोकता है।</li>
<li><strong>विटामिन B12 की कमी होने पर:</strong> डॉक्टर की सलाह पर मिथाइलकोबालामिन (Methylcobalamin) की गोलियां या इंजेक्शन दिए जाते हैं, जिससे कुछ हफ्तों–महीनों में राहत मिलती है।</li>
<li><strong>नस के दर्द (Nerve Pain) के लिए:</strong> तेज झनझनाहट व दर्द में डॉक्टर प्रीगैबालिन (Pregabalin) या गैबापेंटिन (Gabapentin) जैसी नर्व-पेन की दवाएं लिख सकते हैं।</li>
<li><strong>साइटिका / स्लिप डिस्क होने पर:</strong> फिजियोथेरेपी, दवाएं और गंभीर मामलों में नस का दबाव हटाने के लिए इलाज (या सर्जरी) की जरूरत पड़ सकती है।</li>
<li><strong>सहायक दवाएं:</strong> कुछ मामलों में अल्फा-लिपोइक एसिड और न्यूरोट्रॉपिक विटामिन (B1, B6, B12) नसों की मरम्मत में मदद करते हैं।</li>
</ul>

<h3>पैरों में झनझनाहट होने पर कौन सी टेबलेट (Tablet) लेनी चाहिए?</h3>
<p>यह पूरी तरह झनझनाहट के कारण पर निर्भर करता है — विटामिन B12 की कमी में मिथाइलकोबालामिन (Methylcobalamin) की टेबलेट, नस के तेज दर्द में प्रीगैबालिन (Pregabalin) या गैबापेंटिन (Gabapentin), और डायबिटीज में शुगर कंट्रोल करने वाली दवाएं दी जाती हैं। ध्यान रखें — मेडिकल स्टोर से पूछकर कोई भी 'झनझनाहट की टेबलेट' खुद लेना नुकसानदायक हो सकता है, क्योंकि बिना जांच के असली कारण पता ही नहीं चलता और सही इलाज में देरी से नस डैमेज बढ़ता जाता है।</p>
