<h2>पेट में गैस और खट्टी डकार का इलाज (मेडिकल)</h2>
<p>अगर घरेलू उपायों और परहेज से आराम न मिले, तो पेट में गैस और खट्टी डकार का इलाज डॉक्टर की सलाह से इस तरह किया जाता है:</p>
<ul>
<li><strong>एंटासिड:</strong> तुरंत जलन में राहत के लिए एसिड को न्यूट्रल करने वाली दवाएं।</li>
<li><strong>एसिड कम करने वाली दवाएं:</strong> PPI (जैसे ओमेप्राजोल/पैंटोप्राजोल) और H2 ब्लॉकर एसिड बनना कम करते हैं — इन्हें डॉक्टर की सलाह और सही अवधि तक ही लें।</li>
<li><strong>H. pylori का इलाज:</strong> जांच में संक्रमण मिलने पर एंटीबायोटिक का कोर्स दिया जाता है, जिससे बार-बार होने वाली एसिडिटी ठीक होती है।</li>
<li><strong>जांच:</strong> लगातार समस्या में डॉक्टर एंडोस्कोपी, H. pylori टेस्ट या अन्य जांच से असली कारण का पता लगाते हैं।</li>
</ul>
<p><strong>ध्यान दें:</strong> बिना डॉक्टर की सलाह के महीनों तक एंटासिड या एसिड की दवाएं खुद न लें — इससे असली कारण छुप सकता है और लंबे समय में नुकसान हो सकता है।</p>

