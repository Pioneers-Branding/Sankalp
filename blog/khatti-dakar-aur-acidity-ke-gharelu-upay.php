<h4 class="text-danger font-weight-bold"><i class="fas fa-exclamation-triangle me-2"></i> डॉक्टर के पास कब जाएं?</h4>
<p class="mb-0 text-dark">एसिडिटी को हमेशा मामूली न समझें। तुरंत डॉक्टर से मिलें अगर — सीने में दर्द बाजू, कंधे, गर्दन या जबड़े तक फैल रहा हो, पसीना या सांस फूलना साथ हो (यह हार्ट अटैक जैसा गंभीर संकेत हो सकता है, एसिडिटी नहीं); उल्टी में खून आए या मल काला/टार जैसा हो; खाना या पानी निगलने में कठिनाई या दर्द हो; बिना कारण वजन तेजी से घट रहा हो; या भूख न लगना, लगातार उल्टी और कमजोरी बनी रहे। ये लक्षण दिल की बीमारी या पेट-आंत की गंभीर समस्या (अल्सर, रुकावट) की ओर इशारा कर सकते हैं और तुरंत जांच जरूरी है।</p>
