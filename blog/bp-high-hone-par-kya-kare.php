<h2>BP कितना होना चाहिए? (Normal BP Chart)</h2>
<p>सामान्य व्यक्ति का बीपी लगभग <strong>120/80 mmHg</strong> होना चाहिए। अगर आप सोच रहे हैं कि <strong>हाई बीपी कितना होता है</strong> — तो लगातार <strong>140/90 mmHg या उससे अधिक</strong> रीडिंग आने पर उसे हाई ब्लड प्रेशर (हाइपरटेंशन) माना जाता है। बीपी को दो अंकों में मापा जाता है:</p>
<ul>
<li><strong>सिस्टोलिक (Systolic BP) - ऊपर का अंक:</strong> यह तब का दबाव है जब हृदय सिकुड़ता है और शरीर में रक्त धकेलता है।</li>
<li><strong>डायस्टोलिक (Diastolic BP) - नीचे का अंक:</strong> यह तब का दबाव है जब हृदय दो धड़कनों के बीच आराम करता है और रक्त से भरता है।</li>
</ul>

