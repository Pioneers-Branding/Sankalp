<h4 class="text-danger font-weight-bold"><i class="fas fa-exclamation-triangle me-2"></i> डॉक्टर के पास कब जाएं?</h4>
<p class="mb-0 text-dark">ज्यादातर छाले खतरनाक नहीं होते, लेकिन कुछ लक्षण गंभीर हो सकते हैं। तुरंत डॉक्टर से मिलें अगर — कोई छाला या घाव 2–3 हफ्ते में भी न भरे; मुंह या जीभ पर दर्द रहित सफेद या लाल दाग (Patch) या गांठ बनकर बढ़ रही हो; बहुत बड़े या असामान्य रूप से कई छाले एक साथ हों; या आप तंबाकू, गुटखा, सुपारी या शराब का सेवन करते हों। ऐसे मामलों में मुंह के कैंसर (Oral Cancer) की आशंका को दूर करने के लिए डॉक्टर से जांच कराना बहुत जरूरी है — जल्दी पहचान होने पर इलाज आसान और असरदार होता है।</p>
