<li><strong>रीढ़ की हड्डी में ट्यूमर:</strong> दुर्लभ लेकिन संभव कारण।</li>
<li><strong>संक्रमण:</strong> गर्दन की हड्डी या ऊतकों में संक्रमण।</li>
